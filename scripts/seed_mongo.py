"""Seed the Cairn MongoDB with users, DBA runbooks, and execution history.

Idempotent by construction: every document gets a deterministic ``_id`` derived
from a natural key (user email, runbook slug, execution index) via ``det_oid``.
Re-running ``replace_one(..., upsert=True)`` therefore overwrites documents in
place instead of creating duplicates, and cross-references (``owner_id``,
``related_runbook_ids``) resolve to those same deterministic ids.

Run:  python -m scripts.seed_mongo
(Requires MONGO_URI to point at a reachable MongoDB, e.g.
 mongodb://localhost:27017/cairn)
"""

