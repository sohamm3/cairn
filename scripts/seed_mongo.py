def seed_users(users_coll, user_ids: dict[str, ObjectId]) -> None:
    for user in USERS:
        _id = det_oid("user", user["email"])
        user_ids[user["email"]] = _id
        users_coll.replace_one({"_id": _id}, {"_id": _id, **user}, upsert=True)


def seed_runbooks(runbooks_coll, user_ids: dict[str, ObjectId]) -> None:
    for i, rb in enumerate(RUNBOOKS):
        _id = det_oid("runbook", rb["slug"])
        created = BASE + timedelta(days=i)
        doc = {
            "_id": _id,
            "title": rb["title"],
            "slug": rb["slug"],
            "category": rb["category"],
            "severity": rb["severity"],
            "summary": rb["summary"],
            "steps": build_steps(rb["steps"]),
            "tags": rb["tags"],
            "related_runbook_ids": [det_oid("runbook", s) for s in rb["related"]],
            "owner_id": user_ids[rb["owner"]],
            "created_at": created,
            "updated_at": created + timedelta(days=3, hours=i),
        }
        runbooks_coll.replace_one({"_id": _id}, doc, upsert=True)


def seed_execution_log(exec_coll) -> None:
    slugs = [rb["slug"] for rb in RUNBOOKS]
    emails = [u["email"] for u in USERS]
    # Weighted toward success, with some failed/aborted runs.
    outcomes = ["success", "success", "success", "failed", "aborted"]
    for i in range(30):
        # Step of 7 is coprime with 20, so all runbooks get covered.
        slug = slugs[(i * 7) % len(slugs)]
        email = emails[i % len(emails)]
        _id = det_oid("exec", str(i))
        doc = {
            "_id": _id,
            "runbook_id": det_oid("runbook", slug),
            "operator_id": det_oid("user", email),
            "ran_at": BASE + timedelta(days=40) - timedelta(hours=i * 5, minutes=(i * 13) % 60),
            "outcome": outcomes[i % len(outcomes)],
        }
        exec_coll.replace_one({"_id": _id}, doc, upsert=True)


def main() -> None:
    db = get_mongo()
    users_coll = db["users"]
    runbooks_coll = db["runbooks"]
    exec_coll = db["execution_log"]

    users_coll.create_index("email", unique=True)
    runbooks_coll.create_index("slug", unique=True)
    exec_coll.create_index([("runbook_id", ASCENDING), ("ran_at", ASCENDING)])

    user_ids: dict[str, ObjectId] = {}
    seed_users(users_coll, user_ids)
    seed_runbooks(runbooks_coll, user_ids)
    seed_execution_log(exec_coll)

    print(f"users:         {users_coll.count_documents({})}")
    print(f"runbooks:      {runbooks_coll.count_documents({})}")
    print(f"execution_log: {exec_coll.count_documents({})}")

    print("\n--- sample runbooks ---")
    for slug in ("pg-recover-replica-wal-lag", "redis-diagnose-latency-spikes"):
        doc = runbooks_coll.find_one({"slug": slug})
        print(json_util.dumps(doc, indent=2))


if __name__ == "__main__":
    main()
