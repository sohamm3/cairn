def det_oid(*parts: str) -> ObjectId:
    """Deterministic ObjectId from a natural key, keeping seeding idempotent."""
    digest = hashlib.md5("::".join(parts).encode()).hexdigest()
    return ObjectId(digest[:24])


def build_steps(raw: list[tuple[str, str | None, str | None]]) -> list[dict]:
    """Turn (instruction, command, expected_result) tuples into ordered steps."""
    return [
        {
            "order": i,
            "instruction": instruction,
            "command": command,
            "expected_result": expected_result,
        }
        for i, (instruction, command, expected_result) in enumerate(raw, start=1)
    ]


